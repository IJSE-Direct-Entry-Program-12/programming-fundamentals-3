void main(){
    System.out.println("Direct Entry\r Program");   // Carriage Return
    System.out.println("Hello DEP-1200\b\b!");      // Backspace
    System.out.println("Hello\n\nThis is awesome"); // New Line
    System.out.println("Direct\tEntry\tProgram");   // Tab  space
    System.out.println("Direct\sEntry\sProgram");   // Space
    System.out.println("Direct Entry Program");
    System.out.println("Direct\f Entry\f Program"); // Form Feed

    System.out.println("\\");
    System.out.println("\"");
    System.out.println("'");
    System.out.println('a');
    System.out.println('\'');
}